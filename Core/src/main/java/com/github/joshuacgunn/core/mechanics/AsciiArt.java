    /**
     * Gets a random wilderness ASCII art.
     *
     * @return A random wilderness scene
     */
    public static String getRandomWildernessArt() {
        String[] wilderness = {
                LOGO_WILDERNESS_1,
                LOGO_WILDERNESS_2,
                LOGO_WILDERNESS_3,
                LOGO_WILDERNESS_4
        };

        int randIndex = new Random().nextInt(wilderness.length);
        return wilderness[randIndex];
    }

    /**
     * Gets location-specific ASCII art.
     *
     * @param location The location to get art for
     * @return ASCII art for the specified location type
     */
    public static String getArtForLocation(Location location, GameState gameState) {
        if (gameState instanceof CombatState) {
            return LOGO_COMBAT;
        }

        if (location instanceof Town) {
            return LOGO_TOWN;
        } else if (location instanceof Dungeon) {
            return LOGO_DUNGEON;
        } else if (location instanceof Shop) {
            return LOGO_SHOP;
        } else {
            return getRandomWildernessArt();
        }
    }
